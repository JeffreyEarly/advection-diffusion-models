cacheDirectory = string(options.cacheDirectory);
if strlength(cacheDirectory) == 0
    cacheDirectory = string(fullfile(scriptDir, "CacheData"));
end

cachePath = fullfile(cacheDirectory, site1OscroftFairComparisonCacheFilename( ...
    nBootstraps=options.nBootstraps, ...
    randomSeed=options.randomSeed, ...
    scoreStride=options.scoreStride, ...
    oldDof=options.oldDof, ...
    oldModelIndex=options.oldModelIndex, ...
    psiS=options.psiS, ...
    fastS=options.fastS, ...
    mesoscaleConstraint=options.mesoscaleConstraint, ...
    shouldLoadPublishedBaseline=options.shouldLoadPublishedBaseline));
end

function comparison = loadCachedSite1OscroftFairComparison(cachePath, options)
comparison = [];
if ~isfile(cachePath)
    return
end

try
    fileData = load(cachePath, "comparison");
catch
    return
end

if ~isfield(fileData, "comparison") || ~isValidCachedSite1OscroftFairComparison(fileData.comparison, options)
    return
end

comparison = fileData.comparison;
end

function tf = isValidCachedSite1OscroftFairComparison(comparison, options)
tf = isstruct(comparison) && ...
    isfield(comparison, "settings") && ...
    isfield(comparison, "site") && ...
    isfield(comparison, "old") && ...
    isfield(comparison, "new") && ...
    isfield(comparison, "stageDiagnostics") && ...
    isfield(comparison, "fullFitDiagnostic") && ...
    isfield(comparison, "matchedSharedBootstrapDiagnostic") && ...
    isfield(comparison, "cacheInfo");
if ~tf
    return
end

tf = hasMatchingSetting(comparison.settings, "nBootstraps", options.nBootstraps) && ...
    hasMatchingSetting(comparison.settings, "randomSeed", options.randomSeed) && ...
    hasMatchingSetting(comparison.settings, "scoreStride", options.scoreStride) && ...
    hasMatchingSetting(comparison.settings, "oldDof", options.oldDof) && ...
    hasMatchingSetting(comparison.settings, "oldModelIndex", options.oldModelIndex) && ...
    hasMatchingSetting(comparison.settings, "psiS", options.psiS) && ...
    hasMatchingSetting(comparison.settings, "fastS", options.fastS) && ...
    hasMatchingSetting(comparison.settings, "mesoscaleConstraint", string(options.mesoscaleConstraint)) && ...
    hasMatchingSetting(comparison.settings, "shouldLoadPublishedBaseline", options.shouldLoadPublishedBaseline) && ...
    hasMatchingSetting(comparison.cacheInfo, "cacheVersion", site1OscroftFairComparisonCacheVersion()) && ...
    isfield(comparison.cacheInfo, "siteDataModifiedDatenum") && ...
    isfield(comparison.site, "dataPath") && ...
    isfield(comparison.site, "queryTimes") && ...
    ~isempty(comparison.site.queryTimes);
if ~tf
    return
end

tf = abs(siteDataModifiedDatenum(comparison.site.dataPath) - comparison.cacheInfo.siteDataModifiedDatenum) <= 1e-12;
end

function tf = hasMatchingSetting(settings, fieldName, expectedValue)
tf = isfield(settings, fieldName) && isequal(settings.(fieldName), expectedValue);
end

function value = siteDataModifiedDatenum(dataPath)
if ~isfile(dataPath)
    value = NaN;
    return
end

fileInfo = dir(dataPath);
value = fileInfo.datenum;
end

function value = site1OscroftFairComparisonCacheVersion()
value = 1;
end

function site = loadSite1ComparisonInputs(scriptDir, scoreStride)
dataPath = fullfile(scriptDir, "..", "ExampleData", "LatMix2011", "smoothedGriddedRho1Drifters.mat");
if ~isfile(dataPath)
    error("GriddedStreamfunction:MissingExampleData", ...
        "Expected local example data at %s.", dataPath);
end

siteData = load(dataPath);
t = reshape(siteData.t, [], 1);
x = siteData.x(:, 1:(end - 1));
y = siteData.y(:, 1:(end - 1));
nDrifters = size(x, 2);
trajectories = TrajectorySpline.empty(0, 1);
for iDrifter = 1:nDrifters
    trajectories(end + 1, 1) = TrajectorySpline.fromData(t, x(:, iDrifter), y(:, iDrifter), S=3); %#ok<AGROW>
end

f0 = 2 * 7.2921e-5 * sin(siteData.lat0*pi/180);
queryTimes = t;
scoreIndices = 1:scoreStride:numel(queryTimes);
scoreTimes = queryTimes(scoreIndices);

site = struct( ...
    "dataPath", dataPath, ...
    "t", t, ...
    "x", x, ...
    "y", y, ...
    "trajectories", trajectories, ...
    "nDrifters", nDrifters, ...
    "f0", f0, ...
    "queryTimes", queryTimes, ...
    "scoreTimes", scoreTimes, ...
    "scoreIndices", scoreIndices, ...
    "scoreStride", scoreStride);
end

function bootstrapIndices = deterministicBootstrapIndices(nDrifters, nBootstraps, randomSeed)
originalRng = rng;
cleanupRng = onCleanup(@() rng(originalRng));
rng(randomSeed);
bootstrapIndices = randi(nDrifters, nBootstraps, nDrifters);
end

function ensemble = buildLinearVelocityFieldEnsemble(site, bootstrapIndices, options)
arguments
    site struct
    bootstrapIndices {mustBeNumeric, mustBeReal, mustBeFinite}
    options.dof (1,1) double {mustBeInteger, mustBePositive}
    options.modelIndex (1,1) double {mustBeInteger, mustBeMember(options.modelIndex, 1:7)} = 3
    options.historicalScores = []
end

parametersToEstimate = modelParametersForIndex(options.modelIndex);
[fullParameters, ~] = EstimateLinearVelocityFieldParameters(site.x, site.y, site.t, parametersToEstimate, options.dof);
fullOldSummary = linearSummaryFromParameters(fullParameters);
fullCommonSummary = transformLinearSummaryToCommonSpace( ...
    fullOldSummary, mean(site.x, 2), mean(site.y, 2));

nTime = numel(site.queryTimes);
nBootstraps = size(bootstrapIndices, 1);
oldSummary = emptyLinearSummary(nTime, nBootstraps);
mx = zeros(nTime, nBootstraps);
my = zeros(nTime, nBootstraps);

for iBootstrap = 1:nBootstraps
    sampledIndices = bootstrapIndices(iBootstrap, :);
    xSub = site.x(:, sampledIndices);
    ySub = site.y(:, sampledIndices);
    parameters = EstimateLinearVelocityFieldParameters(xSub, ySub, site.t, parametersToEstimate, options.dof);
    summary = linearSummaryFromParameters(parameters);
    oldSummary.u0(:, iBootstrap) = summary.u0;
    oldSummary.v0(:, iBootstrap) = summary.v0;
    oldSummary.sigma_n(:, iBootstrap) = summary.sigma_n;
    oldSummary.sigma_s(:, iBootstrap) = summary.sigma_s;
    oldSummary.zeta(:, iBootstrap) = summary.zeta;
    oldSummary.delta(:, iBootstrap) = summary.delta;
    mx(:, iBootstrap) = mean(xSub, 2);
    my(:, iBootstrap) = mean(ySub, 2);
end

commonSummary = transformLinearSummaryToCommonSpace(oldSummary, mx, my);
if isempty(options.historicalScores)
    historicalScores = EstimateSolutionLikelihoodFromBootstraps(oldSummary, parametersToEstimate, site.scoreStride);
else
    historicalScores = reshape(options.historicalScores, 1, []);
end
commonScores = computeComparisonConsensusScores(commonSummary, site.scoreIndices, "zeroVorticity");

ensemble = struct( ...
    "bootstrapIndices", bootstrapIndices, ...
    "parametersToEstimate", parametersToEstimate, ...
    "fullOldSummary", fullOldSummary, ...
    "fullCommonSummary", fullCommonSummary, ...
    "oldSummary", oldSummary, ...
    "commonSummary", commonSummary, ...
    "fullDisplaySeries", displaySeriesFromLinearOldSummary(fullOldSummary), ...
    "displaySeries", displaySeriesFromLinearOldSummary(oldSummary), ...
    "fullCommonSeries", commonSeriesFromSummary(fullCommonSummary, fullCommonSummary), ...
    "commonSeries", commonSeriesFromSummary(commonSummary, fullCommonSummary), ...
    "scores", struct( ...
        "historical", historicalScores, ...
        "current", historicalScores, ...
        "common", commonScores.joint, ...
        "commonComponents", commonScores));
end

function ensemble = buildGriddedStreamfunctionEnsemble(site, bootstrapIndices, options)
arguments
    site struct
    bootstrapIndices {mustBeNumeric, mustBeReal, mustBeFinite}
    options.psiS (1,3) double {mustBeInteger, mustBeNonnegative}
    options.fastS (1,1) double {mustBeInteger, mustBeNonnegative}
    options.mesoscaleConstraint {mustBeTextScalar, mustBeMember(options.mesoscaleConstraint, ["none", "zeroVorticity", "zeroStrain"])}
end

fitOptions = struct( ...
    "psiS", options.psiS, ...
    "fastS", options.fastS, ...
    "mesoscaleConstraint", string(options.mesoscaleConstraint), ...
    "buildDecomposition", false);
fitArguments = namedargs2cell(fitOptions);

fullFit = GriddedStreamfunction(site.trajectories, fitArguments{:});
fullSummary = summaryFromGriddedFit(fullFit, site.queryTimes);

nTime = numel(site.queryTimes);
nBootstraps = size(bootstrapIndices, 1);
summary = emptyCommonSummary(nTime, nBootstraps);

for iBootstrap = 1:nBootstraps
    sampledTrajectories = reshape(site.trajectories(bootstrapIndices(iBootstrap, :)), [], 1);
    fit = GriddedStreamfunction(sampledTrajectories, fitArguments{:});
    bootstrapSummary = summaryFromGriddedFit(fit, site.queryTimes);
    summary.uCenter(:, iBootstrap) = bootstrapSummary.uCenter;
    summary.vCenter(:, iBootstrap) = bootstrapSummary.vCenter;
    summary.sigma_n(:, iBootstrap) = bootstrapSummary.sigma_n;
    summary.sigma_s(:, iBootstrap) = bootstrapSummary.sigma_s;
    summary.zeta(:, iBootstrap) = bootstrapSummary.zeta;
end

currentScores = computeComparisonConsensusScores(summary, site.scoreIndices, options.mesoscaleConstraint);
ensemble = struct( ...
    "bootstrapIndices", bootstrapIndices, ...
    "fullSummary", fullSummary, ...
    "fullCommonSummary", fullSummary, ...
    "summary", summary, ...
    "commonSummary", summary, ...
    "fullDisplaySeries", displaySeriesFromGriddedSummary(fullSummary), ...
    "displaySeries", displaySeriesFromGriddedSummary(summary), ...
    "fullCommonSeries", commonSeriesFromSummary(fullSummary, fullSummary), ...
    "commonSeries", commonSeriesFromSummary(summary, fullSummary), ...
    "scores", struct( ...
        "current", currentScores.joint, ...
        "common", currentScores.joint, ...
        "commonComponents", currentScores));
end

function publishedBaseline = loadPublishedBaseline(scriptDir, site, options)
