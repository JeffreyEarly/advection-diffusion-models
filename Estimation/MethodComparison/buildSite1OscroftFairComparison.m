newCommonScores = computeComparisonConsensusScores(newBootstrap.summary, site.scoreIndices, options.mesoscaleConstraint);
newEnsemble = struct( ...
    "bootstrapIndices", newBootstrap.bootstrapIndices, ...
    "fullSummary", newBootstrap.fullSummary, ...
    "fullCommonSummary", newBootstrap.fullSummary, ...
    "summary", newBootstrap.summary, ...
    "commonSummary", newBootstrap.summary, ...
    "fullDisplaySeries", displaySeriesFromGriddedSummary(newBootstrap.fullSummary), ...
    "displaySeries", displaySeriesFromGriddedSummary(newBootstrap.summary), ...
    "fullCommonSeries", commonSeriesFromSummary(newBootstrap.fullSummary, newBootstrap.fullSummary), ...
    "commonSeries", commonSeriesFromSummary(newBootstrap.summary, newBootstrap.fullSummary), ...
    "scores", struct( ...
        "current", newBootstrap.scores.joint, ...
        "common", newCommonScores.joint, ...
        "commonComponents", newCommonScores));

publishedBaseline = struct( ...
    "available", true, ...
    "old", oldEnsemble, ...
    "new", newEnsemble, ...
    "paths", struct("oldBootstrapPath", oldBootstrapPath));
end

function summary = linearSummaryFromParameters(parameters)
summary = struct( ...
    "u0", reshape(parameters.u0, [], 1), ...
    "v0", reshape(parameters.v0, [], 1), ...
    "sigma_n", reshape(parameters.sigma_n, [], 1), ...
    "sigma_s", reshape(parameters.sigma_s, [], 1), ...
    "zeta", reshape(parameters.zeta, [], 1), ...
    "delta", reshape(parameters.delta, [], 1));
end

function commonSummary = transformLinearSummaryToCommonSpace(summary, mx, my)
mx = reshapeLike(mx, summary.u0);
my = reshapeLike(my, summary.v0);
commonSummary = struct( ...
    "uCenter", summary.u0 + 0.5 * (summary.sigma_n + summary.delta) .* mx + 0.5 * (summary.sigma_s - summary.zeta) .* my, ...
    "vCenter", summary.v0 + 0.5 * (summary.sigma_s + summary.zeta) .* mx + 0.5 * (summary.delta - summary.sigma_n) .* my, ...
    "sigma_n", summary.sigma_n, ...
    "sigma_s", summary.sigma_s, ...
    "zeta", summary.zeta);
end

function values = reshapeLike(values, reference)
if isequal(size(values), size(reference))
    return
end

if isvector(values) && size(reference, 1) == numel(values)
    values = repmat(reshape(values, [], 1), 1, size(reference, 2));
    return
end

error("GriddedStreamfunction:ComparisonShapeMismatch", ...
    "Values with size %s cannot be expanded to match reference size %s.", ...
    mat2str(size(values)), mat2str(size(reference)));
end

function centers = bootstrapCenterMean(values, bootstrapIndices)
nTime = size(values, 1);
nBootstraps = size(bootstrapIndices, 1);
centers = zeros(nTime, nBootstraps);
for iBootstrap = 1:nBootstraps
    centers(:, iBootstrap) = mean(values(:, bootstrapIndices(iBootstrap, :)), 2);
end
end

function summary = summaryFromGriddedFit(fit, queryTimes)
mx = reshape(fit.centerOfMassTrajectory.x(queryTimes), [], 1);
my = reshape(fit.centerOfMassTrajectory.y(queryTimes), [], 1);
summary = struct( ...
    "uCenter", reshape(fit.uMesoscale(queryTimes, mx, my), [], 1), ...
    "vCenter", reshape(fit.vMesoscale(queryTimes, mx, my), [], 1), ...
    "sigma_n", reshape(fit.sigma_n(queryTimes, mx, my), [], 1), ...
    "sigma_s", reshape(fit.sigma_s(queryTimes, mx, my), [], 1), ...
    "zeta", reshape(fit.zeta(queryTimes, mx, my), [], 1));
end

