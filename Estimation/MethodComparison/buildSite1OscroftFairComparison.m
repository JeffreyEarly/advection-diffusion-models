function comparison = buildSite1OscroftFairComparison(options)
arguments
    options.nBootstraps (1,1) double {mustBeInteger, mustBePositive} = 1000
    options.randomSeed (1,1) double {mustBeInteger, mustBeFinite} = 0
    options.scoreStride (1,1) double {mustBeInteger, mustBePositive} = 6
    options.oldDof (1,1) double {mustBeInteger, mustBePositive} = 4
    options.oldModelIndex (1,1) double {mustBeInteger, mustBeMember(options.oldModelIndex, 1:7)} = 3
    options.psiS (1,3) double {mustBeInteger, mustBeNonnegative} = [2 2 3]
    options.fastS (1,1) double {mustBeInteger, mustBeNonnegative} = 3
    options.mesoscaleConstraint {mustBeTextScalar, mustBeMember(options.mesoscaleConstraint, ["none", "zeroVorticity", "zeroStrain"])} = "zeroVorticity"
    options.shouldLoadPublishedBaseline (1,1) logical = true
    options.shouldUseCache (1,1) logical = true
    options.shouldRefreshCache (1,1) logical = false
    options.cacheDirectory {mustBeTextScalar} = ""
    options.displaySummary (1,1) logical = true
end

scriptDir = fileparts(mfilename("fullpath"));
cachePath = "";
if options.shouldUseCache
    cachePath = site1OscroftFairComparisonCachePath(scriptDir, options);
    if ~options.shouldRefreshCache
        comparison = loadCachedSite1OscroftFairComparison(cachePath, options);
        if ~isempty(comparison)
            comparison.cacheInfo.wasLoadedFromCache = true;
            comparison.cacheInfo.cachePath = string(cachePath);
            if options.displaySummary
                displayComparisonSummary(comparison);
            end
            return
        end
    end
end

site = loadSite1ComparisonInputs(scriptDir, options.scoreStride);
sharedBootstrapIndices = deterministicBootstrapIndices(site.nDrifters, options.nBootstraps, options.randomSeed);

oldShared = buildLinearVelocityFieldEnsemble( ...
    site, ...
    sharedBootstrapIndices, ...
    dof=options.oldDof, ...
    modelIndex=options.oldModelIndex, ...
    historicalScores=[]);
newShared = buildGriddedStreamfunctionEnsemble( ...
    site, ...
    sharedBootstrapIndices, ...
    psiS=options.psiS, ...
    fastS=options.fastS, ...
    mesoscaleConstraint=options.mesoscaleConstraint);

publishedBaseline = struct("available", false);
stageDiagnostics = repmat(emptyStageDiagnostic(), 0, 1);
if options.shouldLoadPublishedBaseline
    publishedBaseline = loadPublishedBaseline( ...
        scriptDir, ...
        site, ...
        dof=options.oldDof, ...
        modelIndex=options.oldModelIndex, ...
        psiS=options.psiS, ...
        fastS=options.fastS, ...
        mesoscaleConstraint=options.mesoscaleConstraint);
    if publishedBaseline.available
        stageDiagnostics(end + 1) = stageDiagnosticFromRankedSeries( ... %#ok<AGROW>
            "Current published comparison", ...
            "published display", ...
            publishedBaseline.old.displaySeries, ...
            publishedBaseline.new.displaySeries, ...
            publishedBaseline.old.scores.historical, ...
            publishedBaseline.new.scores.current);
        stageDiagnostics(end + 1) = stageDiagnosticFromRankedSeries( ... %#ok<AGROW>
            "Variable-space aligned comparison", ...
            "published aligned", ...
            publishedBaseline.old.commonSeries, ...
            publishedBaseline.new.commonSeries, ...
            publishedBaseline.old.scores.historical, ...
            publishedBaseline.new.scores.current);
    end
end

stageDiagnostics(end + 1) = stageDiagnosticFromRankedSeries( ... %#ok<AGROW>
    "Shared-resample comparison", ...
    "shared draws", ...
    oldShared.commonSeries, ...
    newShared.commonSeries, ...
    oldShared.scores.historical, ...
    newShared.scores.current);
stageDiagnostics(end + 1) = stageDiagnosticFromRankedSeries( ... %#ok<AGROW>
    "Shared-score comparison", ...
    "shared draws + common score", ...
    oldShared.commonSeries, ...
    newShared.commonSeries, ...
    oldShared.scores.common, ...
    newShared.scores.common);

comparison = struct( ...
    "settings", struct( ...
        "nBootstraps", options.nBootstraps, ...
        "randomSeed", options.randomSeed, ...
        "scoreStride", options.scoreStride, ...
        "oldDof", options.oldDof, ...
        "oldModelIndex", options.oldModelIndex, ...
        "psiS", options.psiS, ...
        "fastS", options.fastS, ...
        "mesoscaleConstraint", string(options.mesoscaleConstraint), ...
        "shouldLoadPublishedBaseline", options.shouldLoadPublishedBaseline), ...
    "site", site, ...
    "sharedBootstrapIndices", sharedBootstrapIndices, ...
    "old", struct("shared", oldShared), ...
    "new", struct("shared", newShared), ...
    "publishedBaseline", publishedBaseline, ...
    "stageDiagnostics", stageDiagnostics, ...
    "fullFitDiagnostic", seriesDiagnostic(oldShared.fullCommonSeries, newShared.fullCommonSeries), ...
    "matchedSharedBootstrapDiagnostic", matchedBootstrapDiagnostic(oldShared.commonSeries, newShared.commonSeries), ...
    "cacheInfo", struct( ...
        "cacheVersion", site1OscroftFairComparisonCacheVersion(), ...
        "cachePath", string(cachePath), ...
        "wasLoadedFromCache", false, ...
        "siteDataModifiedDatenum", siteDataModifiedDatenum(site.dataPath)));

if options.shouldUseCache
    cacheDir = fileparts(cachePath);
    if exist(cacheDir, "dir") == 0
        mkdir(cacheDir);
    end
    save(cachePath, "comparison");
end

if options.displaySummary
    displayComparisonSummary(comparison);
end
end

