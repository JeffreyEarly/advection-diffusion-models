function parameters = modelParametersForIndex(modelIndex)
switch modelIndex
    case 1
        parameters = [ModelParameter.u0v0, ModelParameter.vorticity];
    case 2
        parameters = [ModelParameter.u0v0, ModelParameter.divergence];
    case 3
        parameters = [ModelParameter.u0v0, ModelParameter.strain];
    case 4
        parameters = [ModelParameter.u0v0, ModelParameter.vorticity, ModelParameter.divergence];
    case 5
        parameters = [ModelParameter.u0v0, ModelParameter.strain, ModelParameter.vorticity];
    case 6
        parameters = [ModelParameter.u0v0, ModelParameter.strain, ModelParameter.divergence];
    case 7
        parameters = [ModelParameter.u0v0, ModelParameter.strain, ModelParameter.vorticity, ModelParameter.divergence];
end
end

function diagnostic = stageDiagnosticFromRankedSeries(stageName, stageMode, oldSeries, newSeries, oldScores, newScores)
[~, oldIndex] = max(oldScores);
[~, newIndex] = max(newScores);
diagnostic = emptyStageDiagnostic();
diagnostic.stage = string(stageName);
diagnostic.mode = string(stageMode);
diagnostic.oldIndex = oldIndex;
diagnostic.newIndex = newIndex;

seriesDiagnosticStruct = seriesDiagnostic( ...
    selectSeriesColumn(oldSeries, oldIndex), ...
    selectSeriesColumn(newSeries, newIndex));
diagnostic.uRms = seriesDiagnosticStruct.uRms;
diagnostic.vRms = seriesDiagnosticStruct.vRms;
diagnostic.uvRms = seriesDiagnosticStruct.uvRms;
diagnostic.sigmaNRms = seriesDiagnosticStruct.sigmaNRms;
diagnostic.sigmaSRms = seriesDiagnosticStruct.sigmaSRms;
diagnostic.strainRms = seriesDiagnosticStruct.strainRms;
diagnostic.thetaRms = seriesDiagnosticStruct.thetaRms;
end

function diagnostic = matchedBootstrapDiagnostic(oldSeries, newSeries)
diagnostic = struct( ...
    "uRms", rmsDifference(oldSeries.u, newSeries.u), ...
    "vRms", rmsDifference(oldSeries.v, newSeries.v), ...
    "uvRms", rmsDifference([oldSeries.u(:); oldSeries.v(:)], [newSeries.u(:); newSeries.v(:)]), ...
    "sigmaNRms", rmsDifference(oldSeries.sigma_n, newSeries.sigma_n), ...
    "sigmaSRms", rmsDifference(oldSeries.sigma_s, newSeries.sigma_s), ...
    "strainRms", rmsDifference([oldSeries.sigma_n(:); oldSeries.sigma_s(:)], [newSeries.sigma_n(:); newSeries.sigma_s(:)]), ...
    "thetaRms", rmsDifference(oldSeries.theta, newSeries.theta));
end

