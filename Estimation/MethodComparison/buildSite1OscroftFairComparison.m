oldCommonScores = computeComparisonConsensusScores(oldCommonSummary, site.scoreIndices, "zeroVorticity");
oldEnsemble = struct( ...
    "bootstrapIndices", oldData.drifterPermutations, ...
    "fullOldSummary", oldFullSummary, ...
    "fullCommonSummary", transformLinearSummaryToCommonSpace(oldFullSummary, mean(site.x, 2), mean(site.y, 2)), ...
    "oldSummary", oldBootstrap, ...
    "commonSummary", oldCommonSummary, ...
    "fullDisplaySeries", displaySeriesFromLinearOldSummary(oldFullSummary), ...
    "displaySeries", displaySeriesFromLinearOldSummary(oldBootstrap), ...
    "fullCommonSeries", commonSeriesFromSummary( ...
        transformLinearSummaryToCommonSpace(oldFullSummary, mean(site.x, 2), mean(site.y, 2)), ...
        transformLinearSummaryToCommonSpace(oldFullSummary, mean(site.x, 2), mean(site.y, 2))), ...
    "commonSeries", commonSeriesFromSummary(oldCommonSummary, transformLinearSummaryToCommonSpace(oldFullSummary, mean(site.x, 2), mean(site.y, 2))), ...
    "scores", struct( ...
        "historical", reshape(oldBootstrap.jointlikelihood, 1, []), ...
        "current", reshape(oldBootstrap.jointlikelihood, 1, []), ...
        "common", oldCommonScores.joint, ...
        "commonComponents", oldCommonScores));

newBootstrap = loadOrBuildPublishedGriddedBootstrap( ...
    scriptDir, ...
    site.trajectories, ...
    nBootstraps=1000, ...
    randomSeed=0, ...
    scoreStride=site.scoreStride, ...
    psiS=options.psiS, ...
    fastS=options.fastS, ...
    mesoscaleConstraint=options.mesoscaleConstraint);
if ~isequal(newBootstrap.queryTimes, site.queryTimes) || ~isequal(newBootstrap.scoreTimes, site.scoreTimes)
    error("GriddedStreamfunction:PublishedBootstrapTimeMismatch", ...
        "Published bootstrap query/score times do not match the Site 1 comparison times.");
