        function site1SmokeTestRunsAndStaysInSmallDifferenceRegime(testCase)
            helperPath = which("buildSite1OscroftFairComparison");
            dataPath = fullfile(fileparts(helperPath), "..", "ExampleData", "LatMix2011", "smoothedGriddedRho1Drifters.mat");
            if ~isfile(dataPath)
                testCase.assumeFail(sprintf("Missing local example data at %s.", dataPath))
            end

            comparison = buildSite1OscroftFairComparison( ...
                nBootstraps=6, ...
                randomSeed=5, ...
                shouldLoadPublishedBaseline=false, ...
                shouldUseCache=false, ...
                displaySummary=false);

            testCase.verifyEqual(numel(comparison.stageDiagnostics), 2)
            testCase.verifyTrue(all(isfinite(comparison.old.shared.commonSummary.uCenter), "all"))
            testCase.verifyTrue(all(isfinite(comparison.old.shared.commonSummary.vCenter), "all"))
            testCase.verifyTrue(all(isfinite(comparison.new.shared.summary.uCenter), "all"))
            testCase.verifyTrue(all(isfinite(comparison.new.shared.summary.vCenter), "all"))
            testCase.verifyLessThanOrEqual(comparison.fullFitDiagnostic.uvRms, 5e-3)
            testCase.verifyLessThanOrEqual(comparison.fullFitDiagnostic.strainRms, 3e-6)
            testCase.verifyLessThanOrEqual(comparison.matchedSharedBootstrapDiagnostic.uvRms, 1.5e-2)
            testCase.verifyLessThanOrEqual(comparison.matchedSharedBootstrapDiagnostic.strainRms, 3e-6)
        end

        function site1FairComparisonCacheCanBeReused(testCase)
            helperPath = which("buildSite1OscroftFairComparison");
            dataPath = fullfile(fileparts(helperPath), "..", "ExampleData", "LatMix2011", "smoothedGriddedRho1Drifters.mat");
            if ~isfile(dataPath)
                testCase.assumeFail(sprintf("Missing local example data at %s.", dataPath))
            end

            cacheDirectory = tempname;
            mkdir(cacheDirectory);
            testCase.addTeardown(@() rmdir(cacheDirectory, "s"));

            comparisonBuilt = buildSite1OscroftFairComparison( ...
                nBootstraps=4, ...
                randomSeed=9, ...
                shouldLoadPublishedBaseline=false, ...
                shouldRefreshCache=true, ...
                cacheDirectory=cacheDirectory, ...
                displaySummary=false);

            cachePath = fullfile(cacheDirectory, site1OscroftFairComparisonCacheFilename( ...
                nBootstraps=4, ...
                randomSeed=9, ...
                scoreStride=6, ...
                oldDof=4, ...
                oldModelIndex=3, ...
                psiS=[2 2 3], ...
                fastS=3, ...
                mesoscaleConstraint="zeroVorticity", ...
                shouldLoadPublishedBaseline=false));
            testCase.verifyTrue(isfile(cachePath))
            testCase.verifyFalse(comparisonBuilt.cacheInfo.wasLoadedFromCache)

            comparisonCached = buildSite1OscroftFairComparison( ...
                nBootstraps=4, ...
                randomSeed=9, ...
                shouldLoadPublishedBaseline=false, ...
                cacheDirectory=cacheDirectory, ...
                displaySummary=false);

            testCase.verifyTrue(comparisonCached.cacheInfo.wasLoadedFromCache)
            testCase.verifyEqual(comparisonCached.settings, comparisonBuilt.settings)
            testCase.verifyEqual(comparisonCached.old.shared.commonSummary.uCenter, comparisonBuilt.old.shared.commonSummary.uCenter, "AbsTol", 1e-12)
            testCase.verifyEqual(comparisonCached.new.shared.summary.uCenter, comparisonBuilt.new.shared.summary.uCenter, "AbsTol", 1e-12)
        end

