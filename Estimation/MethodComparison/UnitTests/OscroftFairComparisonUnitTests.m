    methods (TestClassSetup)
        function addComparisonFolderToPath(testCase)
            comparisonDir = fileparts(fileparts(mfilename("fullpath")));
            addpath(comparisonDir);
            testCase.addTeardown(@() rmpath(comparisonDir));
        end
