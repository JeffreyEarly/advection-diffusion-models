    methods (Test)
        function sharedBootstrapComparisonAgreesForZeroVorticityLinearField(testCase)
            [t, x, y, trajectories] = OscroftFairComparisonUnitTests.zeroVorticityLinearFieldData();
            bootstrapIndices = OscroftFairComparisonUnitTests.deterministicBootstrapIndices(size(x, 2), 6, 13);
            scoreIndices = 1:2:numel(t);

            oldEnsemble = OscroftFairComparisonUnitTests.linearCommonSpaceEnsemble(x, y, t, bootstrapIndices);
            newEnsemble = OscroftFairComparisonUnitTests.griddedCommonSpaceEnsemble(trajectories, t, bootstrapIndices);

            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.fullSummary.uCenter - newEnsemble.fullSummary.uCenter), [], "all"), 5e-3)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.fullSummary.vCenter - newEnsemble.fullSummary.vCenter), [], "all"), 5e-3)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.fullSummary.sigma_n - newEnsemble.fullSummary.sigma_n), [], "all"), 2e-11)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.fullSummary.sigma_s - newEnsemble.fullSummary.sigma_s), [], "all"), 2e-11)

            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.summary.uCenter - newEnsemble.summary.uCenter), [], "all"), 8e-3)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.summary.vCenter - newEnsemble.summary.vCenter), [], "all"), 8e-3)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.summary.sigma_n - newEnsemble.summary.sigma_n), [], "all"), 2e-11)
            testCase.verifyLessThanOrEqual(max(abs(oldEnsemble.summary.sigma_s - newEnsemble.summary.sigma_s), [], "all"), 2e-11)

            referenceScores = computeComparisonConsensusScores(oldEnsemble.summary, scoreIndices, "zeroVorticity");
            copiedScores = computeComparisonConsensusScores(struct( ...
                "uCenter", oldEnsemble.summary.uCenter, ...
                "vCenter", oldEnsemble.summary.vCenter, ...
                "sigma_n", oldEnsemble.summary.sigma_n, ...
                "sigma_s", oldEnsemble.summary.sigma_s, ...
                "zeta", oldEnsemble.summary.zeta), scoreIndices, "zeroVorticity");
            testCase.verifyEqual(referenceScores.joint, copiedScores.joint, "AbsTol", 1e-12)
        end

        function thetaBranchAlignmentCollapsesEquivalentNinetyDegreeBranches(testCase)
            thetaReference = deg2rad((10:5:30)');
            thetaBootstrap = [thetaReference, thetaReference + pi/2, thetaReference - pi/2];
            sigma_n = cos(2 * thetaBootstrap);
            sigma_s = sin(2 * thetaBootstrap);

            rawTheta = atan2(sigma_s, sigma_n) * 90/pi;
            alignedTheta = alignPrincipalStrainAngleToReference( ...
                sigma_n, sigma_s, cos(2 * thetaReference), sin(2 * thetaReference), units="degrees");

            rawWidth = max(rawTheta, [], 2) - min(rawTheta, [], 2);
            alignedWidth = max(alignedTheta, [], 2) - min(alignedTheta, [], 2);

            testCase.verifyGreaterThan(max(rawWidth), 80)
            testCase.verifyLessThanOrEqual(max(alignedWidth), 1e-10)
        end

        function strainKdeComparisonResolvesRequestedDays(testCase)
            comparison = OscroftFairComparisonUnitTests.syntheticStrainKdeComparison();
            kdeComparison = buildSite1BootstrapStrainKdeComparison(comparison, [0.15 1.9 3.2]);

            testCase.verifyEqual(kdeComparison.timeIndices, [1; 3; 4])
            testCase.verifyEqual(kdeComparison.matchedTimeDays, [0; 2; 3], "AbsTol", 1e-12)

            testCase.verifyError( ...
                @() buildSite1BootstrapStrainKdeComparison(comparison, [-0.1 1.0]), ...
                "GriddedStreamfunction:ComparisonTimeOutOfRange")
            testCase.verifyError( ...
                @() buildSite1BootstrapStrainKdeComparison(comparison, [0.1 0.2]), ...
                "GriddedStreamfunction:DuplicateComparisonTimeIndex")
        end

