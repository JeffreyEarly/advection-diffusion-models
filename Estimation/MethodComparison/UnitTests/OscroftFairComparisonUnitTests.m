                testCase.verifyTrue(all(isfinite(panel.contourLevels)))
                testCase.verifyEqual(panel.gridVectors{1}(1), kdeComparison.bounds.minimum(1), "AbsTol", 1e-12)
                testCase.verifyEqual(panel.gridVectors{1}(end), kdeComparison.bounds.maximum(1), "AbsTol", 1e-12)
                testCase.verifyEqual(panel.gridVectors{2}(1), kdeComparison.bounds.minimum(2), "AbsTol", 1e-12)
                testCase.verifyEqual(panel.gridVectors{2}(end), kdeComparison.bounds.maximum(2), "AbsTol", 1e-12)
            end
        end

