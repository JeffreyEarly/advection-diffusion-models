    methods (Static)
        function [t, x, y, trajectories] = zeroVorticityLinearFieldData()
            model = LinearVelocityField(sigma=4.0e-6, theta=pi/9, zeta=0, u0=0.08, v0=-0.05);
            integrator = AdvectionDiffusionIntegrator(model, 0);
            [x0, y0] = ndgrid([-600 0 600] + 1200, [-400 0 400] - 800);
            [t, x, y] = integrator.particleTrajectories(x0(:), y0(:), 12*3600, 900);
            trajectories = GriddedStreamfunctionBootstrapUnitTests.trajectorySplinesFromMatrices(t, x, y);
        end

        function bootstrapIndices = deterministicBootstrapIndices(nDrifters, nBootstraps, randomSeed)
            originalRng = rng;
            cleanupRng = onCleanup(@() rng(originalRng));
            rng(randomSeed);
            bootstrapIndices = randi(nDrifters, nBootstraps, nDrifters);
        end

        function ensemble = linearCommonSpaceEnsemble(x, y, t, bootstrapIndices)
            parametersToEstimate = [ModelParameter.u0v0, ModelParameter.strain];
            [fullParameters, ~] = EstimateLinearVelocityFieldParameters(x, y, t, parametersToEstimate, 4);
            fullSummary = OscroftFairComparisonUnitTests.linearCommonSummary(fullParameters, mean(x, 2), mean(y, 2));

            nTime = numel(t);
            nBootstraps = size(bootstrapIndices, 1);
            summary = struct( ...
                "uCenter", zeros(nTime, nBootstraps), ...
                "vCenter", zeros(nTime, nBootstraps), ...
                "sigma_n", zeros(nTime, nBootstraps), ...
                "sigma_s", zeros(nTime, nBootstraps), ...
                "zeta", zeros(nTime, nBootstraps));

            for iBootstrap = 1:nBootstraps
                sampledIndices = bootstrapIndices(iBootstrap, :);
                xSub = x(:, sampledIndices);
                ySub = y(:, sampledIndices);
                parameters = EstimateLinearVelocityFieldParameters(xSub, ySub, t, parametersToEstimate, 4);
                bootstrapSummary = OscroftFairComparisonUnitTests.linearCommonSummary( ...
                    parameters, mean(xSub, 2), mean(ySub, 2));
                summary.uCenter(:, iBootstrap) = bootstrapSummary.uCenter;
                summary.vCenter(:, iBootstrap) = bootstrapSummary.vCenter;
                summary.sigma_n(:, iBootstrap) = bootstrapSummary.sigma_n;
                summary.sigma_s(:, iBootstrap) = bootstrapSummary.sigma_s;
                summary.zeta(:, iBootstrap) = bootstrapSummary.zeta;
            end

            ensemble = struct("fullSummary", fullSummary, "summary", summary);
        end

        function ensemble = griddedCommonSpaceEnsemble(trajectories, queryTimes, bootstrapIndices)
            fitOptions = struct( ...
                "psiS", [2 2 3], ...
                "fastS", 3, ...
                "mesoscaleConstraint", "zeroVorticity", ...
                "buildDecomposition", false);
            fitArguments = namedargs2cell(fitOptions);

            fullFit = GriddedStreamfunction.fromTrajectories(trajectories, fitArguments{:});
            fullSummary = OscroftFairComparisonUnitTests.griddedSummaryFromFit(fullFit, queryTimes);

            nTime = numel(queryTimes);
            nBootstraps = size(bootstrapIndices, 1);
            summary = struct( ...
                "uCenter", zeros(nTime, nBootstraps), ...
                "vCenter", zeros(nTime, nBootstraps), ...
                "sigma_n", zeros(nTime, nBootstraps), ...
                "sigma_s", zeros(nTime, nBootstraps), ...
                "zeta", zeros(nTime, nBootstraps));

            for iBootstrap = 1:nBootstraps
                sampledTrajectories = reshape(trajectories(bootstrapIndices(iBootstrap, :)), [], 1);
                fit = GriddedStreamfunction.fromTrajectories(sampledTrajectories, fitArguments{:});
                bootstrapSummary = OscroftFairComparisonUnitTests.griddedSummaryFromFit(fit, queryTimes);
                summary.uCenter(:, iBootstrap) = bootstrapSummary.uCenter;
                summary.vCenter(:, iBootstrap) = bootstrapSummary.vCenter;
                summary.sigma_n(:, iBootstrap) = bootstrapSummary.sigma_n;
                summary.sigma_s(:, iBootstrap) = bootstrapSummary.sigma_s;
                summary.zeta(:, iBootstrap) = bootstrapSummary.zeta;
            end

            ensemble = struct("fullSummary", fullSummary, "summary", summary);
        end

        function summary = griddedSummaryFromFit(fit, queryTimes)
            mx = reshape(fit.centerOfMassTrajectory.x(queryTimes), [], 1);
            my = reshape(fit.centerOfMassTrajectory.y(queryTimes), [], 1);
            summary = struct( ...
                "uCenter", reshape(fit.uMesoscale(queryTimes, mx, my), [], 1), ...
                "vCenter", reshape(fit.vMesoscale(queryTimes, mx, my), [], 1), ...
                "sigma_n", reshape(fit.sigma_n(queryTimes, mx, my), [], 1), ...
                "sigma_s", reshape(fit.sigma_s(queryTimes, mx, my), [], 1), ...
                "zeta", reshape(fit.zeta(queryTimes, mx, my), [], 1));
        end

        function summary = linearCommonSummary(parameters, mx, my)
            u0 = reshape(parameters.u0, [], 1);
            v0 = reshape(parameters.v0, [], 1);
            sigma_n = reshape(parameters.sigma_n, [], 1);
            sigma_s = reshape(parameters.sigma_s, [], 1);
            zeta = reshape(parameters.zeta, [], 1);
            delta = reshape(parameters.delta, [], 1);
            mx = reshape(mx, [], 1);
            my = reshape(my, [], 1);

            summary = struct( ...
                "uCenter", u0 + 0.5 * (sigma_n + delta) .* mx + 0.5 * (sigma_s - zeta) .* my, ...
                "vCenter", v0 + 0.5 * (sigma_s + zeta) .* mx + 0.5 * (delta - sigma_n) .* my, ...
                "sigma_n", sigma_n, ...
                "sigma_s", sigma_s, ...
                "zeta", zeta);
        end

        function comparison = syntheticStrainKdeComparison()
            queryTimes = (0:4)' * 86400;
            nTime = numel(queryTimes);
            nBootstraps = 6;
            [timeGrid, bootstrapGrid] = ndgrid(1:nTime, 1:nBootstraps);

            oldSigmaN = 1e-6 * (0.4 * timeGrid + 0.25 * cos(bootstrapGrid));
            oldSigmaS = 1e-6 * (-0.2 * timeGrid + 0.35 * sin(bootstrapGrid));
            newSigmaN = oldSigmaN + 0.15e-6;
            newSigmaS = oldSigmaS - 0.10e-6;

            comparison = struct( ...
                "site", struct("queryTimes", queryTimes), ...
                "old", struct("shared", struct( ...
                    "commonSummary", struct("sigma_n", oldSigmaN, "sigma_s", oldSigmaS), ...
                    "fullCommonSummary", struct( ...
                        "sigma_n", mean(oldSigmaN, 2), ...
                        "sigma_s", mean(oldSigmaS, 2)))), ...
                "new", struct("shared", struct( ...
                    "commonSummary", struct("sigma_n", newSigmaN, "sigma_s", newSigmaS), ...
                    "fullCommonSummary", struct( ...
                        "sigma_n", mean(newSigmaN, 2), ...
                        "sigma_s", mean(newSigmaS, 2)))));
        end
    end
